const toTraditional = (message: string): string => {
  const phrased = phraseReplacements.reduce(
    (current, [simplified, traditional]) => current.split(simplified).join(traditional),
    message,
  );
  return Array.from(phrased, (character) => characterReplacements[character] ?? character).join('');
};

export function createTraditionalMessages<T extends Record<string, string>>(
  messages: T,
): { [K in keyof T]: string } {
  return Object.fromEntries(
    Object.entries(messages).map(([key, message]) => [key, toTraditional(message)]),
  ) as { [K in keyof T]: string };
}
